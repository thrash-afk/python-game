# –£–≥–∞–¥–∞–π —Å–ª–æ–≤–æ
# –ö–æ–º–ø—å—é—Ç–µ—Ä –∑–∞–≥–∞–¥—ã–≤–∞–µ—Ç —Å–ª–æ–≤–æ –∏ —Å–æ–æ–±—â–∞–µ—Ç –µ–≥–æ –¥–ª–∏–Ω—É, –∞ –∏–≥—Ä–æ–∫ –¥–æ–ª–∂–µ–Ω –µ–≥–æ —É–≥–∞–¥–∞—Ç—å.
# –£ –∏–≥—Ä–æ–∫–∞ –µ—Å—Ç—å 5 –ø–æ–ø—ã—Ç–æ–∫ —É–∑–Ω–∞—Ç—å, –µ—Å—Ç—å –ª–∏ –∫–∞–∫–∞—è-–ª–∏–±–æ –±—É–∫–≤–∞ –≤ –∑–∞–≥–∞–¥–∞–Ω–Ω–æ–º —Å–ª–æ–≤–µ.

import random


WORDS = ("–ø—Ä–∏–Ω—Ü", "–º–∞–º–∞", "–ø–∞–ø–∞", "—Å–µ—Å—Ç—Ä–∞", "–¥–æ—á—å", "–±—Ä–∞—Ç", "—É—Ç—Ä–æ", "–≤–µ—á–µ—Ä")
word = random.choice(WORDS)
tries = 5
i = 1
letters = ()

print(f"\n–Ø –∑–∞–≥–∞–¥–∞–ª —Å–ª–æ–≤–æ –∏–∑ {len(word)} –±—É–∫–≤. –£ —Ç–µ–±—è –µ—Å—Ç—å {tries} –ø–æ–ø—ã—Ç–æ–∫ —É–∑–Ω–∞—Ç—å, " \
      + "\n–µ—Å—Ç—å –ª–∏ –∫–∞–∫–∞—è-–ª–∏–±–æ –±—É–∫–≤–∞ –≤ –∑–∞–≥–∞–¥–∞–Ω–Ω–æ–º —Å–ª–æ–≤–µ." \
        + "\n–Ø –±—É–¥—É –æ—Ç–≤–µ—á–∞—Ç—å —Ç–æ–ª—å–∫–æ \"–î–∞\" –∏–ª–∏ \"–ù–µ—Ç\" ")

while tries:
    print("–ü–æ–ø—ã—Ç–∫–∞ —É–∑–Ω–∞—Ç—å –±—É–∫–≤—É ‚Ññ", i)
    letter = input("–í–≤–µ–¥–∏—Ç–µ –±—É–∫–≤—É: ")
    if len(letter) > 1 or letter == "":
        print("–ù—É–∂–Ω–æ –≤–≤–µ—Å—Ç–∏ –û–î–ù–£ –±—É–∫–≤—É.")
    else:
        tries -= 1
        i += 1
        if letter in word:
            print("–î–∞")
            if letter not in letters:
                letters += tuple(letter)
        else:
            print("–ù–µ—Ç")

print("–ò—Ç–∞–∫, –≤ —ç—Ç–æ–º —Å–ª–æ–≤–µ –µ—Å—Ç—å –±—É–∫–≤—ã", letters)
print("–°–ª–æ–≤–æ –∏–∑", len(word), "–±—É–∫–≤.")
print("–î–ª—è –≤—ã—Ö–æ–¥–∞ –Ω–∞–∂–º–∏—Ç–µ Enter, –Ω–µ –≤–≤–æ–¥—è –≤–∞—Ä–∏–∞–Ω—Ç –æ—Ç–≤–µ—Ç–∞.\n")

guess = input("–ß—Ç–æ —ç—Ç–æ –∑–∞ —Å–ª–æ–≤–æ?\n")
while guess != word and guess != "":
    print("\n–≠—Ç–æ –Ω–µ —Ç–æ —Å–ª–æ–≤–æ.")
    guess = input("–ß—Ç–æ —ç—Ç–æ –∑–∞ —Å–ª–æ–≤–æ?\n")

if guess == word:
    print("\nüí• –ü–æ–∑–¥—Ä–∞–≤–ª—è—é, —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ —Å–ª–æ–≤–æ:", word.upper())

print("–°–ø–∞—Å–∏–±–æ –∑–∞ –∏–≥—Ä—É.")
