# –ö–æ–º–ø—å—é—Ç–µ—Ä –≤—ã–±–∏—Ä–∞–µ—Ç —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç 1 –¥–æ 100.
# –ò–≥—Ä–æ–∫ –ø—ã—Ç–∞–µ—Ç—Å—è –æ—Ç–≥–∞–¥–∞—Ç—å —ç—Ç–æ —á–∏—Å–ª–æ, –∏ –∫–æ–º–ø—å—é—Ç–µ—Ä –≥–æ–≤–æ—Ä–∏—Ç,
# –ø—Ä–µ–¥–ø–æ–ª–æ–∂–µ–Ω–∏–µ –±–æ–ª—å—à–µ/–º–µ–Ω—å—à–µ, —á–µ–º –∑–∞–≥–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –∏–ª–∏ –ø–æ–ø–∞–ª–æ –≤ —Ç–æ—á–∫—É

import random


print("\t–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∏–≥—Ä—É \"–û—Ç–≥–∞–¥–∞–π —á–∏—Å–ª–æ\"!")
print("\n–Ø –∑–∞–≥–∞–¥–∞–ª –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç 1 –¥–æ 100.")
print("–ü–æ—Å—Ç–∞—Ä–∞–π—Ç–µ—Å—å —É–≥–∞–¥–∞—Ç—å –µ–≥–æ –∑–∞ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫.")

limit = 7
print("–£ –≤–∞—Å –≤—Å–µ–≥–æ", limit, "–ø–æ–ø—ã—Ç–æ–∫.\n")

# –ù–∞—á–∞–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
the_number = random.randint(1, 100)
guess = int(input("–í–∞—à–µ –ø—Ä–µ–¥–ø–æ–ª–æ–∂–µ–Ω–∏–µ: \n"))
tries = 1


# –¶–∏–∫–ª –æ—Ç–≥–∞–¥—ã–≤–∞–Ω–∏—è
while the_number != guess and tries < limit:
    if the_number > guess:
        print("–ë–æ–ª—å—à–µ")
    else:
        print("–ú–µ–Ω—å—à–µ")
    guess = int(input("–í–∞—à–µ –ø—Ä–µ–¥–ø–æ–ª–æ–∂–µ–Ω–∏–µ: \n"))
    tries += 1

if tries <= limit:
    # –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º –ø–æ–±–µ–¥–∏—Ç–µ–ª—è
    print("\n–í–∞–º —É–¥–∞–ª–æ—Å—å –æ—Ç–≥–∞–¥–∞—Ç—å —á–∏—Å–ª–æ! –≠—Ç–æ –∏ –≤ —Å–∞–º–æ–º –¥–µ–ª–µ", the_number)
    print("–í–∞–º –ø–æ–Ω–∞–¥–æ–±–∏–ª–æ—Å—å –≤—Å–µ–≥–æ –ª–∏—à—å", tries, "–ø–æ–ø—ã—Ç–æ–∫!")
else:
    print("\n–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏! –•–∞-—Ö–∞-—Ö–∞ üëπ")

input("\n\n–ù–∞–∂–º–∏—Ç–µ Enter, —á—Ç–æ–±—ã –≤—ã–π—Ç–∏.")
